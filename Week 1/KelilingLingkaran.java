public class HitungLuasSegitiga {
    public static void main(String[] args) {
        
        // Menghitung Luas Segitiga Siku-Siku
        double alas = 6;
        double tinggi = 8;
        double luas_segitiga = (alas * tinggi) / 2;
        System.out.println("Luas segitiga siku-siku dengan alas 6 dan tinggi 8 adalah: " + luas_segitiga);
    }
}
